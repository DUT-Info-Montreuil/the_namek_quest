package universite_paris8.iut.fan.the_namek_quest.model;

public class Bloc {

    private String type;
    private int pv ;
    private int id ;
    public Bloc (){
        this.type = type ;
        this.pv = pv ;
        this.id = id ;
    }

    public String getType () {
        return type ;
    }

    public  int getPv () {
        return pv ;
    }
    public int getId () {
        return id ;
    }




}
